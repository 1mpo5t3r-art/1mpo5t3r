// build-manifest.js
// Scans images/full/ and writes posters.js, which index.html reads to build every carousel.
// Filenames decide everything: "<category>_<label>.jpg"
//   e.g. sport_cleary.jpg      -> category "sport",  label "CLEARY"
//        sport_edwards_1.jpg   -> category "sport",  label "EDWARDS 1"
// Run automatically on deploy (Cloudflare build command) or by hand: node build-manifest.js

const fs = require('fs');
const path = require('path');

const IMG_DIR = path.join('images', 'full');
const OUT_FILE = 'posters.js';
const KNOWN = ['sport', 'gaming', 'studio', 'lab'];   // must match the four sections in index.html
const IMAGE_EXT = new Set(['.jpg', '.jpeg', '.png', '.webp', '.avif']);

function toLabel(rest) {
  // text after the first underscore becomes the on-screen label
  return rest.replace(/_/g, ' ').toUpperCase().trim();
}

function build() {
  if (!fs.existsSync(IMG_DIR)) {
    console.error('No ' + IMG_DIR + ' folder found. Nothing to do.');
    process.exit(1);
  }

  const files = fs.readdirSync(IMG_DIR)
    .filter(f => IMAGE_EXT.has(path.extname(f).toLowerCase()))
    .sort();   // alphabetical, so output is stable every build

  const posters = {};
  KNOWN.forEach(c => (posters[c] = []));
  const skipped = [];

  for (const file of files) {
    const base = file.replace(/\.[^.]+$/, '');     // drop the extension
    const cut = base.indexOf('_');
    if (cut < 0) { skipped.push(file + '  (no underscore to split on)'); continue; }

    const cat = base.slice(0, cut).toLowerCase();
    const rest = base.slice(cut + 1);

    if (!KNOWN.includes(cat)) { skipped.push(file + '  (unknown category "' + cat + '")'); continue; }
    if (!rest) { skipped.push(file + '  (no label after the category)'); continue; }

    posters[cat].push({ src: 'images/full/' + file, label: toLabel(rest) });
  }

  const banner = '// AUTO-GENERATED by build-manifest.js. Do not edit by hand.\n'
               + '// Add or remove files in images/full/, then rebuild.\n';
  fs.writeFileSync(OUT_FILE, banner + 'window.POSTERS = ' + JSON.stringify(posters) + ';\n');

  console.log('Wrote ' + OUT_FILE + '  ->  ' + KNOWN.map(c => c + ': ' + posters[c].length).join(', '));
  if (skipped.length) {
    console.log('\nSkipped ' + skipped.length + ' file(s) (not added to the site):');
    skipped.forEach(s => console.log('  - ' + s));
  }
}

build();
